      <div align="center"> 
        <div class="subtitle"><b>РЕШЕНИЕ</b><br>
          Ученого совета университета от 27.01.04 г. <br>
          Деятельность центра дистанционного обучения и итоги выполнения программы 
	&quot;Развитие системы дистанционного обучения в ИТМО&quot;</div>
      </div>
      <div align=justify>Заслушав и обсудив доклад директора центра дистанционного обучения (ДО) доцента Лямина А.В., Совет <b>отмечает</b>.<br>
      Программа "Развитие системы дистанционного обучения в ИТМО" открыта по решению Ученого совета и имеет своей целью разработку и внедрение в учебный процесс университета современных сетевых компьютерных технологий обучения. Результатом  выполнения второй очереди Программы (2001-2003 г.г.) стало создание и функционирование системы ДО в учебном процессе как специализированной информационно-образовательной среды университета на базе сетевых технологий (Internet) и телекоммуникационной инфраструктуры университета. <br>
      Нормативное, программное, учебно-методическое и техническое сопровождение системы ДО в учебном процессе обеспечивается центром ДО. В структуру центра входят следующие отделы: 
</div>
      <ul>
        <li>программно-технического обеспечения (ПТО); </li>
        <li>организационно-методического обеспечения (ОМО); </li>
        <li>обеспечения учебного процесса (ОУП).</li>
      </ul>
       <div align=justify>Центр оснащен десятью высокопроизводительными серверами, двадцатью рабочими местами сотрудников, компьютерным классом на двадцать посадочных мест для разработчиков электронных учебно-методических ресурсов системы ДО, компьютерным классом на шестьдесят посадочных мест для работы с ресурсами системы. К системе ДО подключено ряд факультетских и кафедральных компьютерных классов.  <br>
        За отчетный период в Программе участвовало более 100 сотрудников вуза, было выполнено 46 проектов на общую сумму 1118000 руб. Разработаны основные конфигурации программных средств (оболочек) для отдельных элементов курсов дистанционного обучения. Информационно-образовательная среда системы ДО ИТМО обеспечивает элементы организации учебного процесса (администрирование, архивирование и др.), поддерживает 53 курса дистанционного обучения (КДО) по дисциплинам различного профиля, включающих обучающие и аттестующие тесты, электронные конспекты, практикумы и виртуальные лаборатории. Наиболее совершенным элементом в системе ДО являются тестовые системы, реализующие развитые формы тестовых заданий и педагогических сценариев. За осенний семестр 2003/2004 учебного года тестированием было охвачено более 3800 студентов, в системе проведено 53525 сеансов аттестаций, во время которых студенты ответили на 750476 вопросов. Результаты аттестаций в системе ДО регулярно передавались в учебный отдел университета. <br>
        Использование системы ДО в учебном процессе позволило унифицировать аттестационные требования по дисциплинам на основе Государственных образовательных стандартов (ГОС), повысить объективность аттестации, а также оценить эффективность профессиональной деятельности преподавателей, активизировать и повысить эффективность самостоятельной работы студентов, интенсифицировать и индивидуализировать процесс обучения.<br>
        Периодически на базе центра ДО проводилось повышение квалификации  преподавателей и сотрудников для работы в среде системы ДО через организацию кратковременных курсов обучения, семинаров и консультаций по использованию сетевых компьютерных технологий в учебном процессе.  <br>
        За период выполнения Программы университет активно участвовал в конференциях и семинарах различного уровня по методам и технологиям дистанционного обучения, было сделано 46 докладов, опубликовано более 60 статей, по профилю Программы обучаются пять аспирантов.<br>
        Вместе с тем, в системе дистанционного обучения имеют место следующие недостатки:
</div>
      <ul>
        <li> качество и полнота курсов ДО не отвечают требованиям учебного процесса;        </li>
        <li> низкая организация учебного процесса с использованием системы, в частности, организация компьютерного тестирования, слабая связь результатов текущего контроля с итоговым;       </li>
        <li> имеют место технические сбои при удаленном доступе к системе.            </li>
      </ul>
       <div align=justify> В целях дальнейшего развития в университете методов и технологий компьютерного обучения, способствующих созданию системы открытого образования, Совет <b>постановляет</b>:
      </div>
      <ol>
        <li>Считать деятельность центра ДО по выполнению второй очереди Программы (ЦКП ДО) на 2001-2003 годы, как обеспечившую создание реально действующей в учебном процессе университета системы дистанционного обучения, удовлетворительной, а программу в целом выполненной.   </li>
	<li>Открыть в университете третью очередь Программы (ЦКП ДО) на 2004-2006 годы, нацеленную на дальнейшее развитие и совершенствование системы ДО ИТМО и с широким привлечением к ее выполнению ППС. О результатах  выполнения Программы совету ЦКП ДО доложить на Ученом совете в январе 2006 года.   </li>
	<li>Заведующим кафедрами в целях обеспечения качества электронных учебно-методических комплексов дисциплин строго соблюдать установленный порядок рецензирования и экспертизы при размещении их в системе ДО.    </li>
	<li>Деканам факультетов осуществлять в установленные сроки контроль текущей успеваемости студентов путем организации и учета результатов компьютерного тестирования в системе ДО.     </li>
	<li>Ученым советам факультетов при обсуждении кандидатур на замещение вакантных должностей профессорско-преподавательского состава рассматривать результаты внедрения системы ДО в учебный процесс.    </li>
	<li>Заведующим кафедрами при подаче заявления на второй срок и более иметь положительное заключение учебно-методического совета университета о внедрении системы ДО по дисциплинам, обеспечиваемыми кафедрой.    </li>
	<li>Ректорату при рассмотрении предложений, проектов, заявлений от структурных подразделений учитывать работу в системе ДО.    </li>
        <li>Ректорату продолжить и развивать практику поощрения сотрудников и структурных подразделений университета за активное участие в разработке и внедрении системы ДО в учебный процесс.    </li>
      </ol>

